#!/usr/bin/env node
/**
 * Build-time fetch of available forms from the forms API.
 *
 * Reads `${VITE_FORMS_API_URL}/form-definitions`, validates the
 * response, and writes a generated TypeScript module exporting the
 * set of available form IDs. The landing app imports that module to
 * decide whether to render Start now buttons at render time.
 *
 * Runs as the `prebuild` and `predev` lifecycle script of the
 * landing app. A genuine failure (unreachable API, malformed response,
 * invalid form IDs) fails the build by design. A successful response
 * with zero forms is a valid empty state: we emit an empty manifest so
 * the build still succeeds and no Start now buttons render until forms
 * exist.
 *
 * See:
 *   - docs/plans/start-now-form-links.md (revision 2)
 *   - docs/decisions/0005-build-time-manifest-for-cross-app-link-availability.md
 */

import { writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const DEFAULT_API_URL = 'https://forms.api.sandbox.alpha.gov.bb'
const FETCH_TIMEOUT_MS = 15_000

const here = dirname(fileURLToPath(import.meta.url))
const outPath = resolve(here, '../src/content/available-forms.gen.ts')

const apiBase = (process.env.VITE_FORMS_API_URL ?? DEFAULT_API_URL).replace(
  /\/+$/,
  '',
)
const endpoint = `${apiBase}/form-definitions`

function fail(message, cause) {
  const lines = [
    '',
    '✗ fetch-form-manifest failed',
    `  endpoint: ${endpoint}`,
    `  reason:   ${message}`,
  ]
  if (cause) lines.push(`  cause:    ${cause}`)
  lines.push('')
  process.stderr.write(lines.join('\n'))
  process.exit(1)
}

async function fetchWithTimeout(url, ms) {
  const controller = new AbortController()
  const timer = setTimeout(() => controller.abort(), ms)
  try {
    return await fetch(url, { signal: controller.signal })
  } finally {
    clearTimeout(timer)
  }
}

const ID_PATTERN = /^[a-z0-9][a-z0-9-]*$/

function assertValidFormIds(ids) {
  if (!Array.isArray(ids)) fail('response.data was not an array')
  for (const id of ids) {
    if (typeof id !== 'string' || !ID_PATTERN.test(id)) {
      fail(`form ID failed validation: ${JSON.stringify(id)}`)
    }
  }
}

function emitManifest(ids) {
  const sorted = [...ids].sort()
  const body =
    `// AUTO-GENERATED by apps/landing/scripts/fetch-form-manifest.mjs\n` +
    `// Do not edit by hand. Regenerate via \`pnpm exec nx run landing:dev\`\n` +
    `// or \`pnpm exec nx run landing:build\`.\n` +
    `//\n` +
    `// Source: ${endpoint}\n` +
    `// Fetched: ${new Date().toISOString()}\n` +
    `\n` +
    `export const AVAILABLE_FORMS: ReadonlySet<string> = new Set([\n` +
    sorted.map((id) => `  ${JSON.stringify(id)},`).join('\n') +
    `\n])\n`
  mkdirSync(dirname(outPath), { recursive: true })
  writeFileSync(outPath, body, 'utf8')
  process.stdout.write(
    `✓ fetch-form-manifest wrote ${sorted.length} form IDs to ${outPath.replace(process.cwd() + '/', '')}\n`,
  )
}

;(async () => {
  let response
  try {
    response = await fetchWithTimeout(endpoint, FETCH_TIMEOUT_MS)
  } catch (err) {
    fail('request failed', err.message ?? String(err))
  }

  if (!response.ok) {
    fail(`HTTP ${response.status} ${response.statusText}`)
  }

  let payload
  try {
    payload = await response.json()
  } catch (err) {
    fail('response was not valid JSON', err.message ?? String(err))
  }

  if (payload?.status !== 'success' || !Array.isArray(payload.data)) {
    fail(
      `unexpected response shape — expected {status:"success", data:[...]} ` +
        `but got status=${JSON.stringify(payload?.status)}`,
    )
  }

  const ids = payload.data.map((entry) => entry?.formId).filter(Boolean)
  assertValidFormIds(ids)
  emitManifest(ids)
})()
